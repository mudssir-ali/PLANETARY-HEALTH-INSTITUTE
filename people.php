<p>Stevi Ibonie is a 1st-year graduate student in the Clinical Psychology PhD program at the University of Colorado, Boulder, as a primary student in the PEP Lab. She obtained her B.A. in Psychology and minor in counseling, clinical, and school psychology from UC Santa Barbara. Previously, Stevi worked with Professor Sheri Johnson, at UC Berkeley studying mood-disorders, where she developed an interest in how social relationships may influence the trajectories of individuals with psychopathologies. During her PhD, she plans to focus on the influence of social functioning and relationships on psychopathologies, with specific interest in mood and behavioral disorders and how positive social relationships with peers may improve the prognosis of individuals experiencing mental health difficulties. Stevi enjoys all kinds of outdoor activities, such as stand up paddle-boarding, hiking, and gardening with the help of her cat.</p>

