<h2><a name="director" id="director"></a>Lab Director</h2>

<div id="picbox"><strong><img src="images/june gruber profile.jpg" alt="Positive Emotion and Psychopathology Lab - People" width="200" height="200" border="0" align="right" class="pplpix"/><p><strong>June Gruber</strong></strong><br />
 <a href="mailto:june.gruber@colorado.edu">Email</a>
 <a href="junegruber.php"><br />
  Webpage</a></p>
</div>

<p>June Gruber is an Associate Professor of Psychology and Neuroscience at the University of Colorado and Director of the Positive Emotion and Psychopathology Laboratory. Dr. Gruber is a licensed clinical psychologist and has published over 100 articles and chapters and has edited the Oxford Handbook of Positive Emotion and Psychopathology and Positive Emotion: Integrating the Light Sides and Dark Sides with Judith Moskowitz. Her work has been recognized by several early-career awards including the Association for Psychological Science’s Rising Star Award and the Janet Taylor Spence Award for Transformative Early Career Contributions, the Society for Research in Psychopathology's Early Career Award, and two NARSAD Young Investigator Awards. Dr. Gruber is currently an Associate Editor and was the former Interim Editor-in-Chief for Perspectives on Psychological Science. Her teaching has been recognized by the 2022 Boulder Faculty Assembly Excellence Award in Teaching & Pedagogy and the 2020 UROP Outstanding Faculty Mentor Award. For more info. see <a href="http://gruberpeplab.com/junegruber.php" target="_blank">Dr. Gruber's webpage</a>.</p>

