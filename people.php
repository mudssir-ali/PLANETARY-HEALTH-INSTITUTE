<p>Cynthia M. Villanueva is a 3rd-year graduate student in the Clinical Psychology PhD program and primary graduate student in the PEP lab at the University of Colorado Boulder. She is also a National Science Foundation Graduate Research Fellowship recipient. Her research interests focus on emotion-related processes in psychopathology, with a particular emphasis on examining individual differences in emotion language and emotion regulation in bipolar and unipolar mood disorders. She is also interested in how stigmas against talking about mental illness and expressed emotions may contribute to health inequities in minoritized communities.</p>

