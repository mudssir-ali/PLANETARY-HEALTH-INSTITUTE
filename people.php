<p>Luiza Rosa is a research coordinator in the PEP Lab. She received her B.S. in Psychology and B.A. Art at the University of California Santa Barbara. After attending cultural psychology class in her undergraduate degree combined with her international student status, Luiza started wondering about how different cultures express verbally and physically different emotional states and how that may affect diagnosis and symptoms of psychopathology. Thus, her main research interest is how different cultural backgrounds (country of origin, ethnicity, race, religion, and so on) can affect and explain why we feel the way we feel. In her free time, Luiza enjoys painting and drawing.</p>



